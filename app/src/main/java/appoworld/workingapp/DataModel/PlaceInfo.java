package appoworld.workingapp.DataModel;

public class PlaceInfo {

    private String name,phone,address,timing,rating,websiteURL,imageURL,description;

    public PlaceInfo() {
    }

    public String getName() {
        return name;
    }

    public String getPhone() {
        return phone;
    }

    public String getAddress() {
        return address;
    }

    public String getTiming() {
        return timing;
    }

    public String getRating() {
        return rating;
    }

    public String getWebsiteURL() {
        return websiteURL;
    }

    public String getImageURL() {
        return imageURL;
    }

    public String getDescription() {
        return description;
    }
}
